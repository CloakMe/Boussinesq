function vdah=d2Mdy2(M,gm,vdah)   %dh operator if gm = 0
          
       j=1;
      vdah(:,j) =  - (2.5-1/12  )*M(:,j) + (16/12)*M(:,j+1) + (-1/12)*M(:,j+2);
      j=2;
      vdah(:,j) = +   (16/12)*M(:,j-1) - (2.5 )*M(:,j)  +  ( 16/12)*M(:,j+1) + (-1/12)*M(:,j+2);
      for j=3:size(M,2)-2
        vdah(:,j) = + (-1/12)*M(:,j-2) +  (16/12)*M(:,j-1) -   (2.5 )*M(:,j)  +  (16/12)*M(:,j+1) + (-1/12)*M(:,j+2);
      end
      vdah(:,end-1) =  + (-1/12)*M(:,end-3)+ (16/12)*M(:,end-2) - (2.5 )*M(:,end-1)  + (16/12)*M(:,end);
      vdah(:,end) =    +  (-1/12)*M(:,end-2) + (16/12)*M(:,end-1) - (2.5  - 1/12 )*M(:,end); 
end